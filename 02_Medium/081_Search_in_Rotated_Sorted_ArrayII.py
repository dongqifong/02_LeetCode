class Solution:
    def search(self, nums, target: int) -> bool:
        left=0
        right=len(nums)-1
        while left<=right:
            m=(left+right)//2
            while left < m and nums[left] == nums[m]: # tricky part
                left += 1
            if nums[left]<=nums[m]:
                res=self.binary_search(nums,left,m,target)
                if res==False:
                    left=m+1
                else:
                    return True
            elif nums[m]<=nums[right]:
                res=self.binary_search(nums,m,right,target)
                if res==False:
                    right=m-1
                else:
                    return True
        return False
    def binary_search(self,nums,left,right,target):
        while left<=right:
            m=(left+right)//2
            if nums[m]<target:
                left=m+1
            elif nums[m]>target:
                right=m-1
            else:
                return True
        return False

